// `` : Template/String` Literals
var name1 = `Sukanta`;
var fullName = `${name1} Ghosh`;

console.log(fullName); //Sukanta Ghosh
console.log(fullName.charAt(0)); //S
console.log(fullName.endsWith("?")); //false
console.log(fullName.includes("Sukanta")); //true
console.log(fullName.toUpperCase()); //SUKANTA GHOSH
